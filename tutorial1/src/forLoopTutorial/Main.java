package forLoopTutorial;

public class Main {

	public static void main(String[] args) {
	
	// This package is regarding For Loops
	
	// basic syntax for a for loop
		for (int i = 0; i > 10; i++) {
		//Each loop, i will increase by one due to the i++. Each time it will Check the value and if larger 
		// Than 10, it will finally stop the loop
			System.out.println(i);
		}
		

	}

}
